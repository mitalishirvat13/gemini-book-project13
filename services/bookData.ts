    'Chetan Bhagat': { category: 'Fiction', author: 'Chetan Bhagat' },
    'How to enjoy Your Life': { category: 'Self-Help', author: 'Dale Carnegie' },
    'How to Devloped Self Confidence': { category: 'Self-Help', author: 'Dale Carnegie' },
